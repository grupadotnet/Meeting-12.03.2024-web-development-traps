const min = Math.min();
const max = Math.max();

console.log('Math.min() < Math.max(): ', min < max);

console.log(`Math.min(): ${min}, Math.max(): ${max}`);
console.log('Minimum value of 1, 2, 3:', Math.min(1, 2, 3));
console.log('Minimum value of 1, 2, 3:', Math.max(1, 2, 3));

